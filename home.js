function updateTable(content){
    let table = document.getElementById("table")

    // Action: Change Table Display
    for (var i = 0, row; row = table.rows[i]; i++) {            // For every row, 
        for (var j = 0, col; col = row.cells[j]; j++) {         // for every column,
            col.innerText = content[i][j];                      // update from array.
        }  
    }
}

